def categorize_terms():
    """按类别分类术语"""
    categories = {
        "components": [],
        "symptoms": [],
        "tools_processes": []
    }
    
    for term, aliases, category, tags, description in new_terms:
        # 根据类别和标签判断归属
        if category in ["硬件相关", "结构相关", "摄像头模组"] and "部件" in tags:
            categories["components"].append((term, term, aliases, category, tags, description))
        elif category == "异常现象":
            categories["symptoms"].append((term, term, aliases, category, tags, description))
        elif category in ["工具", "测试验证", "制造工艺", "流程相关", "性能指标"]:
            categories["tools_processes"].append((term, term, aliases, category, tags, description))
        else:
            # 默认归类到工具流程
            categories["tools_processes"].append((term, term, aliases, category, tags, description))
    
    return categories

def append_to_csv_file(file_path, terms):
    """追加术语到CSV文件"""
    with open(file_path, 'a', encoding='utf-8', newline='') as f:
        writer = csv.writer(f)
        for term, canonical_name, aliases, category, tags, description in terms:
            writer.writerow([term, canonical_name, aliases, category, tags, description])

